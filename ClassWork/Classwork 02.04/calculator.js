// Declare variables
var num1 = 10;
var num2 = 5;
var result;
// Perform arithmetic operations
result = num1 + num2;
console.log("Sum:", result);
result = num1 - num2;
console.log("Difference:", result);
result = num1 * num2;
console.log("Product:", result);
result = num1 / num2;
console.log("Quotient:", result);
// Declare and initialize a string variable
var message = "Hello, World!";
console.log(message);
// Concatenate strings
var additionalString = "Welcome to TypeScript!";
console.log(message + " " + additionalString);
// Working with boolean variables
var isTrue = true;
var isFalse = false;
console.log("isTrue:", isTrue);
console.log("isFalse:", isFalse);
